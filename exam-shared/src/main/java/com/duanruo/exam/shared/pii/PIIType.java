package com.duanruo.exam.shared.pii;

/**
 * PII（个人身份信息）字段类型枚举
 * 
 * @author Augment Agent
 * @since 2025-01-XX
 */
public enum PIIType {
    
    /**
     * 姓名
     * 脱敏规则：保留姓氏，其他用*代替
     * 示例：张三 -> 张*
     */
    NAME,
    
    /**
     * 身份证号
     * 脱敏规则：保留前6位和后4位，中间用*代替
     * 示例：110101199001011234 -> 110101********1234
     */
    ID_CARD,
    
    /**
     * 手机号
     * 脱敏规则：保留前3位和后4位，中间用*代替
     * 示例：13812345678 -> 138****5678
     */
    PHONE,
    
    /**
     * 邮箱
     * 脱敏规则：保留前2位和@后的域名，中间用*代替
     * 示例：zhangsan@example.com -> zh***@example.com
     */
    EMAIL,
    
    /**
     * 银行卡号
     * 脱敏规则：保留前4位和后4位，中间用*代替
     * 示例：6222021234567890 -> 6222********7890
     */
    BANK_CARD,
    
    /**
     * 地址
     * 脱敏规则：保留省市，详细地址用*代替
     * 示例：北京市朝阳区XX路XX号 -> 北京市朝阳区****
     */
    ADDRESS,
    
    /**
     * 自定义
     * 脱敏规则：完全用*代替
     */
    CUSTOM
}

